package com.szbc.model;

public class CarConfiguration {
	private String yearstyleId;
	private String brandId;
	private String brandName;
	private String brandTypeId;
	private String brandType;
	private String modleId;
	private String modleName;
	private String yearstyleName;

	public String getId() {
		return yearstyleId;
	}

	public void setId(String id) {
		this.yearstyleId = id;
	}

	public String getBrandId() {
		return brandId;
	}

	public void setBrandId(String brandId) {
		this.brandId = brandId;
	}

	public String getYearstyleId() {
		return yearstyleId;
	}

	public void setYearstyleId(String yearstyleId) {
		this.yearstyleId = yearstyleId;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getBrandTypeId() {
		return brandTypeId;
	}

	public void setBrandTypeId(String brandTypeId) {
		this.brandTypeId = brandTypeId;
	}

	public String getBrandType() {
		return brandType;
	}

	public void setBrandType(String brandType) {
		this.brandType = brandType;
	}

	public String getModleId() {
		return modleId;
	}

	public void setModleId(String modleId) {
		this.modleId = modleId;
	}

	public String getModleName() {
		return modleName;
	}

	public void setModleName(String modleName) {
		this.modleName = modleName;
	}

	public String getYearstyleName() {
		return yearstyleName;
	}

	public void setYearstyleName(String yearstyleName) {
		this.yearstyleName = yearstyleName;
	}
}
